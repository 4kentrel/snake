#Arithmetic operators - perform simple calculations
x = 67
y = 3

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y) #Modulus returns the remainder

#Comparison operators
a = 57
b = 21
print(a>b)
print(a<b)
print(a>=b)
print(a<=b)
print(a==b) #Equal to
print(a!=b) #Not equal to

#Assignment operators
z = 20
print(z)

num = 2
num *= 23
print(num)
print(15 - 6 + 2 * 4)

